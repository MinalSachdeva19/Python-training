__all__ = ["add", "mul"]

def add(a,b):
    """
    This function performs addition of two numbers.
    :param a: Any integer.
    :param b: Any integer.
    :return: a+b
    """
    return a+b

def sub(c,d):
    """
    This function performs subtraction on two numbers.
    :param c: Any integer.
    :param d: Any integer.
    :return: a-b
    """
    return c-d

def mul(m,n):
    """
    This function performs multiplication of two numbers.
    :param m: Any integer
    :param n: Any integer
    :return: a*b
    """
    return m*n


